    private void lapButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lapButtonActionPerformed
        lapButtonClick = true;

        lapTimeCounter2.reset();
        lapTimeCounter2.start();

        GridValue.lapNumber++;

        GridValue.min_maxCalculation();

        if (GridValue.lapNumber != 1) {
            GridValue.gridDisplay(lapTimeCounter2.lapValues, mainTimeCounter2.lapValues, false);
        } else {
            GridValue.gridDisplay(mainTimeCounter2.lapValues, mainTimeCounter2.lapValues, false);
        }
    }//GEN-LAST:event_lapButtonActionPerformed

