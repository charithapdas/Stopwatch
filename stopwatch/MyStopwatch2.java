    private void start_stop_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_start_stop_ButtonActionPerformed
        lapButton.setEnabled(true);
        mainTimeCounter2.start();
        if (!start_stop_ButtonClick) {
            start_stop_Button.setIcon(new javax.swing.ImageIcon(getClass().getResource("/sIcons/pause_15px.png"))); //main timer has started
            if (lapButtonClick) {
                lapTimeCounter2.start();
            }
            start_stop_ButtonClick = true;
        } else {
            start_stop_Button.setIcon(new javax.swing.ImageIcon(getClass().getResource("/sIcons/play_15px.png"))); //main timer has stopped
            mainTimeCounter2.stop();
            lapTimeCounter2.stop();
            start_stop_ButtonClick = false;
            lapButton.setEnabled(false);
        }
    }//GEN-LAST:event_start_stop_ButtonActionPerformed

    private void maximiseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_maximiseButtonActionPerformed

        MyStopwatch myStopwatch;
        
        if (GridValue.activeFrames || start_stop_ButtonClick) {
            myStopwatch = new MyStopwatch(start_stop_ButtonClick, true);
            
        } else {
            myStopwatch = new MyStopwatch();
        }
        
        MyStopwatch.mainTimeCounter = mainTimeCounter2;
        MyStopwatch.mainTimeCounter.timeCounterMethod(MyStopwatch.totalTime);

        MyStopwatch.lapTimeCounter = lapTimeCounter2;
        MyStopwatch.lapTimeCounter.timeCounterMethod(MyStopwatch.lapTime);
        

        myStopwatch.setVisible(true);
        this.setVisible(false);

    }//GEN-LAST:event_maximiseButtonActionPerformed

